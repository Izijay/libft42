#include "libft.h"

char		*ft_strdup(const char *str)
{
	char	*dest;
	int	i;

	i = 0;
	while (str[i])
		i++;
	if (!(dest = (char *)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
